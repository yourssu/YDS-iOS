extension UIImage {
    fileprivate static func load2(name: String) -> UIImage {
        guard let image = UIImage(named: name, in: ic2.bundle, compatibleWith: nil) else {
            assert(false, "\(name) 이미지 로드 실패")
            return UIImage()
        }
        return image
    }
}
